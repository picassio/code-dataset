"""Heuristic filters for detecting bots, trivial changes, and noise."""

from __future__ import annotations

import re

from ..extraction.models import MergeRecord

# Bot author patterns (case-insensitive)
_BOT_PATTERNS = [
    re.compile(r"\[bot\]$", re.IGNORECASE),
    re.compile(r"^dependabot", re.IGNORECASE),
    re.compile(r"^renovate", re.IGNORECASE),
    re.compile(r"^github-actions", re.IGNORECASE),
    re.compile(r"^semantic-release", re.IGNORECASE),
    re.compile(r"^greenkeeper", re.IGNORECASE),
    re.compile(r"^snyk-bot", re.IGNORECASE),
    re.compile(r"^codecov", re.IGNORECASE),
    re.compile(r"^mergify", re.IGNORECASE),
    re.compile(r"^auto-merge", re.IGNORECASE),
]

# Trivial commit message patterns
_TRIVIAL_MESSAGES = re.compile(
    r"^(?:wip|fix|update|bump|typo|lint|format|style|chore|misc|minor|cleanup|clean up|"
    r"initial commit|merge|revert|undo|temp|tmp|test|testing|debug|todo|fixme|hack|"
    r"oops|nit|whitespace|formatting|reformat|prettier|eslint|rubocop|black|ruff)$",
    re.IGNORECASE,
)

# Auto-generated file patterns
_AUTOGEN_PATTERNS = [
    re.compile(r"\.lock$"),
    re.compile(r"package-lock\.json$"),
    re.compile(r"yarn\.lock$"),
    re.compile(r"pnpm-lock\.yaml$"),
    re.compile(r"Pipfile\.lock$"),
    re.compile(r"poetry\.lock$"),
    re.compile(r"Cargo\.lock$"),
    re.compile(r"go\.sum$"),
    re.compile(r"\.min\.(js|css)$"),
    re.compile(r"dist/"),
    re.compile(r"build/"),
    re.compile(r"node_modules/"),
    re.compile(r"vendor/"),
    re.compile(r"__pycache__/"),
    re.compile(r"\.egg-info/"),
    re.compile(r"\.generated\."),
    re.compile(r"\.pb\.go$"),
    re.compile(r"_generated\.go$"),
]


def is_bot_author(author: str, exclude_authors: list[str] | None = None) -> bool:
    """Check if an author is a bot.

    Args:
        author: Author name string.
        exclude_authors: Additional author names to exclude.

    Returns:
        True if the author appears to be a bot.
    """
    if exclude_authors:
        for excluded in exclude_authors:
            if excluded.lower() in author.lower():
                return True

    return any(pattern.search(author) for pattern in _BOT_PATTERNS)


def is_trivial_message(message: str, min_length: int = 10) -> bool:
    """Check if a commit message is too trivial to be useful.

    Args:
        message: Commit message.
        min_length: Minimum message length.

    Returns:
        True if the message is trivial.
    """
    first_line = message.strip().split("\n")[0].strip()

    if len(first_line) < min_length:
        return True

    if _TRIVIAL_MESSAGES.match(first_line):
        return True

    return False


def is_autogenerated_file(path: str) -> bool:
    """Check if a file path is auto-generated or should be excluded.

    Args:
        path: File path.

    Returns:
        True if the file appears to be auto-generated.
    """
    return any(pattern.search(path) for pattern in _AUTOGEN_PATTERNS)


def has_only_autogenerated_files(record: MergeRecord) -> bool:
    """Check if all files in a merge are auto-generated.

    Args:
        record: The merge record to check.

    Returns:
        True if ALL files are auto-generated (record should be excluded).
    """
    if not record.files_changed:
        return True
    return all(is_autogenerated_file(f.path) for f in record.files_changed)


def is_whitespace_only_diff(diff: str) -> bool:
    """Check if a diff contains only whitespace changes.

    Compares the stripped content of removed lines vs added lines.
    If they produce the same text, the diff is whitespace-only.

    Args:
        diff: Unified diff text.

    Returns:
        True if all changes are whitespace-only.
    """
    removed: list[str] = []
    added: list[str] = []
    has_changes = False

    for line in diff.split("\n"):
        if line.startswith("+") and not line.startswith("+++"):
            added.append(line[1:].strip())
            has_changes = True
        elif line.startswith("-") and not line.startswith("---"):
            removed.append(line[1:].strip())
            has_changes = True

    if not has_changes:
        return True

    return removed == added


def has_only_binary_files(record: MergeRecord) -> bool:
    """Check if all file changes are binary files.

    Args:
        record: The merge record to check.

    Returns:
        True if ALL files are binary.
    """
    if not record.files_changed:
        return True
    return all(f.is_binary for f in record.files_changed)


def is_dependency_update(record: MergeRecord) -> bool:
    """Check if a merge is purely a dependency/version update.

    Args:
        record: The merge record to check.

    Returns:
        True if this appears to be a dependency update.
    """
    dep_keywords = ("bump", "update dep", "upgrade dep", "dependency", "dependencies")
    msg_lower = record.merge_message.lower()

    if any(kw in msg_lower for kw in dep_keywords):
        # Check if all changed files are dependency-related
        dep_files = {
            "package.json",
            "package-lock.json",
            "yarn.lock",
            "pnpm-lock.yaml",
            "Pipfile",
            "Pipfile.lock",
            "pyproject.toml",
            "poetry.lock",
            "requirements.txt",
            "Cargo.toml",
            "Cargo.lock",
            "go.mod",
            "go.sum",
            "Gemfile",
            "Gemfile.lock",
            "build.gradle",
            "pom.xml",
        }
        return all(f.path.split("/")[-1] in dep_files or is_autogenerated_file(f.path) for f in record.files_changed)

    return False
