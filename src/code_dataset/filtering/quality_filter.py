"""Quality filters for merge records.

Applies size limits, file type filters, and message quality checks
to produce a clean set of merge records suitable for dataset generation.
"""

from __future__ import annotations

import fnmatch
import logging
from pathlib import Path

from ..config import Config
from ..extraction.models import MergeRecord
from .heuristics import (
    has_only_autogenerated_files,
    has_only_binary_files,
    is_bot_author,
    is_dependency_update,
    is_trivial_message,
    is_whitespace_only_diff,
)
from .security_filter import redact_secrets, scan_secrets

logger = logging.getLogger(__name__)


def _matches_any_pattern(path: str, patterns: list[str]) -> bool:
    """Check if a path matches any of the glob/fnmatch patterns."""
    name = Path(path).name
    for pattern in patterns:
        if fnmatch.fnmatch(path, pattern):
            return True
        if fnmatch.fnmatch(name, pattern):
            return True
        # Directory patterns: "vendor/" matches "vendor/foo.py"
        if pattern.endswith("/") and path.startswith(pattern):
            return True
    return False


def _has_excluded_extension(path: str, extensions: list[str]) -> bool:
    """Check if a file has an excluded extension."""
    suffix = Path(path).suffix.lower()
    return suffix in extensions


def filter_file_changes(record: MergeRecord, config: Config) -> MergeRecord:
    """Remove excluded files from a merge record.

    Modifies the record in-place, removing files that match exclude patterns.

    Args:
        record: The merge record.
        config: Application config with filter settings.

    Returns:
        The same record with excluded files removed.
    """
    exclude_paths = config.exclude_paths
    exclude_exts = config.exclude_extensions

    original_count = len(record.files_changed)
    record.files_changed = [
        f
        for f in record.files_changed
        if not _matches_any_pattern(f.path, exclude_paths) and not _has_excluded_extension(f.path, exclude_exts)
    ]

    removed = original_count - len(record.files_changed)
    if removed > 0:
        logger.debug(
            "Filtered %d/%d files from %s",
            removed,
            original_count,
            record.id,
        )

    return record


def should_exclude(record: MergeRecord, config: Config) -> str | None:
    """Check if a merge record should be excluded entirely.

    Args:
        record: The merge record to check.
        config: Application config.

    Returns:
        Reason string if excluded, None if the record should be kept.
    """
    # Bot authors
    for author in record.authors:
        if is_bot_author(author, config.exclude_authors):
            return f"bot_author:{author}"

    # Empty diff
    if not record.diff.strip():
        return "empty_diff"

    # All files auto-generated
    if has_only_autogenerated_files(record):
        return "all_autogenerated"

    # All files binary
    if has_only_binary_files(record):
        return "all_binary"

    # Whitespace-only changes
    if is_whitespace_only_diff(record.diff):
        return "whitespace_only"

    # Dependency-only update
    if is_dependency_update(record):
        return "dependency_update"

    # Trivial merge message (only for squash merges where message is the main signal)
    if record.merge_type.value == "squash":
        if is_trivial_message(record.merge_message, config.min_commit_message_length):
            return "trivial_message"

    return None


def filter_records(records: list[MergeRecord], config: Config) -> list[MergeRecord]:
    """Apply all filters to a list of merge records.

    Args:
        records: Raw merge records.
        config: Application config.

    Returns:
        Filtered list of merge records.
    """
    filtered: list[MergeRecord] = []
    excluded_reasons: dict[str, int] = {}

    for record in records:
        # Filter individual file changes first
        filter_file_changes(record, config)

        # Check if the whole record should be excluded
        reason = should_exclude(record, config)
        if reason:
            excluded_reasons[reason] = excluded_reasons.get(reason, 0) + 1
            continue

        # No remaining files after filtering
        if not record.files_changed:
            excluded_reasons["no_files_after_filter"] = excluded_reasons.get("no_files_after_filter", 0) + 1
            continue

        # Redact secrets if enabled
        if config.remove_secrets:
            secrets_found = scan_secrets(record.diff)
            if secrets_found:
                logger.info(
                    "Redacting %d secrets from %s",
                    len(secrets_found),
                    record.id,
                )
                record.diff = redact_secrets(record.diff)
                # Also redact file contents
                for fc in record.files_changed:
                    if fc.content_before:
                        fc.content_before = redact_secrets(fc.content_before)
                    if fc.content_after:
                        fc.content_after = redact_secrets(fc.content_after)

        filtered.append(record)

    # Log exclusion summary
    total_excluded = sum(excluded_reasons.values())
    if total_excluded > 0:
        logger.info(
            "Filtered out %d/%d records: %s",
            total_excluded,
            len(records),
            ", ".join(f"{k}={v}" for k, v in sorted(excluded_reasons.items())),
        )

    logger.info("Kept %d/%d records after filtering", len(filtered), len(records))
    return filtered
